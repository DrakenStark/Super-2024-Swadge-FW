static void mainMenuCb(const char* label, bool selected, uint32_t settingVal)
{
	if( label == mainMenuMode.modeName )
	{
		switchToSwadgeMode( &mainMenuMode );
	}
}

void sandbox_main(void)
{
	ESP_LOGI( "sandbox", "Running from IRAM. %d", global_i );

	menu = initMenu("test ch32v", mainMenuCb);
	addSingleItemToMenu(menu, mainMenuMode.modeName);

	// Load a font
	loadFont("logbook.font", &logbook, false);

	// Initialize menu renderer
	renderer = initMenuLogbookRenderer(&logbook);

	ESP_LOGI( "sandbox", "Loaded" );

	int r = Setup003( &state );
	ESP_LOGI( "sandbox", "CH32V003 Setup: %d", r );
	r = WriteImage003( &state, ws2812bdemo_bin, ws2812bdemo_bin_len );
	ESP_LOGI( "sandbox", "CH32V003 Write: %d", r );
	r = Boot003( &state );
	ESP_LOGI( "sandbox", "CH32V003 Boot: %d", r );
}

void sandbox_exit()
{
	if( menu )
	{
		deinitMenu(menu);
	}

	// Free the font
	freeFont(&logbook);

	// Deinit renderer
	deinitMenuLogbookRenderer(renderer);
}

void sandbox_tick(int64_t elapsedUs)
{
	//uint32_t start = getCycleCount();
	//uint32_t end = getCycleCount();

	buttonEvt_t evt = {0};
	while (checkButtonQueueWrapper(&evt))
		menu = menuButton(menu, evt);
	drawMenuLogbook(menu, renderer, elapsedUs);

	char cts[128];
	sprintf( cts, "%llu\n", elapsedUs ); 
	drawText( &logbook, c555, cts, 180, 30);
}


